export const generateLocalAscii = (text: string): string => {
  const lines = ["", "", "", "", "", ""];
  
  for (const char of text) {
    // Map lower to upper for this specific font if missing
    let key = char;
    if (!STANDARD_FONT[key]) {
      key = char.toUpperCase();
    }
    
    const charLines = STANDARD_FONT[key] || STANDARD_FONT['?'];
    
    for (let i = 0; i < 6; i++) {
      lines[i] += (charLines[i] || "     ") + " "; // Add spacing
    }
  }

  return lines.join('\n');
};
